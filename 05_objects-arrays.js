//배열과 객체
//for -> '배열','객체'
const a1 = "apple";
const a2 = "banana";
const a3 = "orange";
console.log(a1, a2, a3);

const fruits = ["apple", "banana", "orange"];
console.log(fruits);

console.log(fruits[0]);
console.log(fruits[1]);
console.log(fruits[2]);

fruits[2] = "grape";
console.log(fruits);

console.log(fruits.length);

